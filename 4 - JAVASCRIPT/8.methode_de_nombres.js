//floor appel la valeur entiere basse (arondi bas) majorant
console.log(Math.floor(4.9)); //sol

// la console affiche
4

//ceil appel la valeur entiere haute (arondi haut) minorant
console.log(Math.ceil(4.9)); //plafond

// la console affiche
5

//round donne la valeur arondie
console.log(Math.round(4.9));

// la console affiche
5

