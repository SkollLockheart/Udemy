//Concatenation de plusieurs variables
var first = "Salut "; //espace à la fin de la string
var second = "tout le monde";

var third = first + second;
console.log(third)

//La console affiche
"Salut tout le monde"

//Concatenation de plus valeur sur une même ligne
var first = "Salut " + " " + "tout le monde";

console.log(first)

//La console affiche
"Salut tout le monde"

